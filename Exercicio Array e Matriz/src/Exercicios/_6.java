package Exercicios;

public class _6 {
public static void main(String[] args) {
	
	int[] num = new int[6];
	num[0] = 0;
	num[1] = 1;
	num[2] = 2;
	num[3] = 3;
	num[4] = 4;
	num[5] = 5;
	
	
	 
	
	 for (int i = 0; i < num.length; i++) {
		    System.out.println(num[i]);
		    
	 }
	 
	 for (int z = 5; z < num.length && z > -1; z--) {
		    System.out.println(num[z]);

}
}
}
