package _3;

public abstract class Quadrilateros implements IFormasGeometricas {

	private int lado1;
	private int lado2;
	private int lado3;
	private int lado4;
	
	
	public Quadrilateros(int lado1, int lado2, int lado3, int lado4) {
		super();
		this.lado1 = lado1;
		this.lado2 = lado2;
		this.lado3 = lado3;
		this.lado4 = lado4;
	}
	
	
	
}
